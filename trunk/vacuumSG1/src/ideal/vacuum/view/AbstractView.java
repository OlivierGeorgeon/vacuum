package ideal.vacuum.view;

import ideal.vacuum.controller.Controller ;
import ideal.vacuum.controller.MainController ;

import java.awt.event.ActionEvent ;
import java.awt.event.MouseEvent ;
import java.awt.event.MouseListener ;

import javax.swing.AbstractAction ;


public abstract class AbstractView extends AbstractAction implements
		View, MouseListener {

	private final MainController controller;
	protected boolean isDisplay = false;

	public AbstractView(MainController controller) {
		this.controller = controller;
	}

	@Override
	public Controller getController() {
		return this.controller ;
	}

	@Override
	public boolean isDisplay() {
		return this.isDisplay ;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * java.awt.event.ActionListener#actionPerformed(java.awt.event.ActionEvent)
	 */
	public void actionPerformed(ActionEvent event) {
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.awt.event.MouseListener#mouseClicked(java.awt.event.MouseEvent)
	 */
	public void mouseClicked(MouseEvent e) {
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.awt.event.MouseListener#mouseEntered(java.awt.event.MouseEvent)
	 */
	public void mouseEntered(MouseEvent e) {
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.awt.event.MouseListener#mouseExited(java.awt.event.MouseEvent)
	 */
	public void mouseExited(MouseEvent e) {
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.awt.event.MouseListener#mousePressed(java.awt.event.MouseEvent)
	 */
	public void mousePressed(MouseEvent e) {
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * java.awt.event.MouseListener#mouseReleased(java.awt.event.MouseEvent)
	 */
	public void mouseReleased(MouseEvent e) {
	}
}
